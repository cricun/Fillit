#include "fillit.h"

int	hash_count(char tmp[FIG_SIZE + 1][FIG_SIZE + 1])
{
	int r;
	int	c;
	int a;

	r = -1;
	c = -1;
	a = 0;	
	while (++r < 4)
	{
		while (++c < 4)
		{
			if (tmp[r][c] == '#')
				a++;
		}
		c = -1;
	}
	return (a);
}
